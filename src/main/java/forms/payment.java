    private void btnPayNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayNowActionPerformed
        boolean cardSelected = false;
        if (rbtnMastercard.isSelected() == true || rbtnVISA.isSelected() == true){
            cardSelected = true;
        }
        String[] details = {txtCardNumber.getText()};
        
        boolean pass = true;
        for(String detail : details){
            if (detail.equals("")){
                pass = false;
            }
        }
        
        if (pass == true && cardSelected == true){
            int consumed = Integer.parseInt(txtConsumption.getText());
            String type = cbxServiceRate.getSelectedItem().toString();
            String month = cbxMonth.getSelectedItem().toString();
            float amount = getAmount(consumed, type, month);
            lblTotal.setText(String.valueOf(amount));
           
            showMessageDialog(null, "Payment successfully made. Thank you "
                    + "for using our program.");
            
            btnDownload.setEnabled(true);
        } else{
            showMessageDialog(null, "Please check that all fields for the "
                    + "card details are filled", "Missing Values", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnPayNowActionPerformed

