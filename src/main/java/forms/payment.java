    private void btnDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownloadActionPerformed
        JFileChooser f = new JFileChooser();
        f.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        f.showSaveDialog(null);
        
        String dir = f.getSelectedFile().toString();
        System.out.println(dir);
        
        btnDownload.setVisible(false);
        try {
            BufferedImage img = new BufferedImage(jPanel3.getWidth(), jPanel3.getHeight(), BufferedImage.TYPE_INT_RGB);
            jPanel3.paint(img.getGraphics());
            String fname = cbxMonth.getSelectedItem().toString() + 
                    "-" + mainmen.lastName + ", " + mainmen.firstName;
            File outputfile = new File(dir + "\\bill.png");
//            File outputfile = new File(dir + "\\" + fname + ".png");
            ImageIO.write(img, "png", outputfile);
        } catch (IOException e) {
            System.out.println("ERROR: " + e);
        }
        JOptionPane.showMessageDialog(this, "Your bill report is saved at\n" +
                dir + ".",
                "Saved", JOptionPane.PLAIN_MESSAGE);
        btnDownload.setVisible(true);
    }//GEN-LAST:event_btnDownloadActionPerformed

